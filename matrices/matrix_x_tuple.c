#include "../miniRT.h"

t_tuple	matrix_x_tuple(double **m, t_tuple t)
{
	t_tuple	tuple;

	tuple.x = (m[0][0] * t.x) + (m[0][1] * t.y) \
	+ (m[0][2] * t.z) + (m[0][3] * t.w);
	tuple.y = (m[1][0] * t.x) + (m[1][1] * t.y) \
	+ (m[1][2] * t.z) + (m[1][3] * t.w);
	tuple.z = (m[2][0] * t.x) + (m[2][1] * t.y) \
	+ (m[2][2] * t.z) + (m[2][3] * t.w);
	tuple.w = (m[3][0] * t.x) + (m[3][1] * t.y) \
	+ (m[3][2] * t.z) + (m[3][3] * t.w);
	return (tuple);
}
