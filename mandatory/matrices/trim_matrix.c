#include "../miniRT.h"

double	**trim_matrix(double **m)
{
	int	i;
	int	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
			if (m[i][j] < EPSILON && m[i][j] > -EPSILON)
				m[i][j] = 0.0;
	}
	return (m);
}
