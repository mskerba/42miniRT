#include "../miniRT.h"

char	*get_token(char *s)
{
	int	i;
	int	j;

	i = -1;
	while (s[++i] && s[i] == ' ')
		;
	j = i;
	while (s[i] && s[i] != ' ')
		i++;
	s[i] = 0;
	return (s + j);
}
